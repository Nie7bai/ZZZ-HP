import type {
  AgentBuffDoc,
  AgentMindscapeRankBuffs,
  BangbooBuffDoc,
  BuffStatModifiers,
  DriveDiscBuffDoc,
  WengineBuffDoc,
} from '@/types/calculator'
import {
  createEmptyBuffStatModifiers,
  createEmptyAgentBasePanel,
  createEmptyMindscapeBuffs,
  createEmptyMindscapeNotes,
  createEmptyRefinementMods,
  createEmptyWengineAdvancedStats,
  createEmptyWengineRefinementBuffs,
} from '@/utils/calculatorUi'

function mods(partial: Partial<BuffStatModifiers> = {}): BuffStatModifiers {
  return { ...createEmptyBuffStatModifiers(), ...partial }
}

function selfTeam(
  self: Partial<BuffStatModifiers> = {},
  team: Partial<BuffStatModifiers> = {},
): AgentMindscapeRankBuffs {
  return { selfMods: mods(self), teamMods: mods(team) }
}

function agentDoc(
  partial: Omit<AgentBuffDoc, 'mindscapeBuffs' | 'supportNeeds' | 'note' | 'mindscapeNotes' | 'basePanel'> & {
    supportNeeds?: AgentBuffDoc['supportNeeds']
    note?: string
    basePanel?: AgentBuffDoc['basePanel']
    mindscapeNotes?: string[]
    rank0?: AgentMindscapeRankBuffs
  },
): AgentBuffDoc {
  const mindscapeBuffs = createEmptyMindscapeBuffs()
  if (partial.rank0) {
    mindscapeBuffs[0] = partial.rank0
  }
  return {
    id: partial.id,
    name: partial.name,
    profession: partial.profession,
    element: partial.element,
    supportNeeds: partial.supportNeeds ?? [],
    avatar_image: partial.avatar_image,
    note: partial.note ?? '',
    basePanel: partial.basePanel ?? createEmptyAgentBasePanel(),
    mindscapeNotes: partial.mindscapeNotes ?? createEmptyMindscapeNotes(),
    mindscapeBuffs,
  }
}

function wengineDoc(
  partial: Omit<WengineBuffDoc, 'refinementBuffs' | 'profession' | 'baseAtk' | 'advancedStats' | 'note'> & {
    profession?: string
    baseAtk?: number
    advancedStats?: WengineBuffDoc['advancedStats']
    refinementBuffs?: AgentMindscapeRankBuffs[]
    note?: string
  },
): WengineBuffDoc {
  return {
    id: partial.id,
    name: partial.name,
    profession: partial.profession ?? '',
    rarity: partial.rarity,
    avatar_image: partial.avatar_image,
    note: partial.note ?? '',
    baseAtk: partial.baseAtk ?? 0,
    advancedStats: partial.advancedStats ?? createEmptyWengineAdvancedStats(),
    fixedBuffs: partial.fixedBuffs,
    refinementBuffs: partial.refinementBuffs ?? createEmptyWengineRefinementBuffs(),
  }
}

function bangbooDoc(
  partial: Omit<BangbooBuffDoc, 'refinementMods'> & {
    refinementMods?: BuffStatModifiers[]
  },
): BangbooBuffDoc {
  return {
    id: partial.id,
    name: partial.name,
    avatar_image: partial.avatar_image,
    fixedMods: partial.fixedMods,
    refinementMods: partial.refinementMods ?? createEmptyRefinementMods(),
  }
}

